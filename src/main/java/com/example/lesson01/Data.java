package com.example.lesson01;

// 일반 자바 빈으로 사용하게 되는 자바 파일
public class Data {
	// 필드
	private int id;
	private String name;
	
	// 메소드	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	// 만드는게 가능.
}
